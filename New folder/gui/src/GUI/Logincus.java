    private void login() throws Exception{
        
    Connection con = classdb.getCon();
        PreparedStatement ps = null ;
        ResultSet rs = null ;
        try {
            String log =  "SELECT * FROM user WHERE UserName = ? AND Password = ? ;";
            ps = con.prepareStatement(log);
            ps.setString(1, user.getText());
            ps.setString(2, pass.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                fname2logcus = rs.getString("FName");
                lname2logcus = rs.getString("LName");
                JOptionPane.showMessageDialog(null, "Welcome "+fname2logcus+" "+lname2logcus);
                //
                this.setVisible(false);
                playmusic();
//                loadingWelcome lw = new loadingWelcome();
//                lw.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid Username or Password" , "Access Denied",JOptionPane.ERROR_MESSAGE);
            
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e,"",JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                rs.close();
                ps.close();
                con.close();
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data was not been closed " +e,"",JOptionPane.ERROR_MESSAGE);
            }
    }
    }
